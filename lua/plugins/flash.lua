-- lua/plugins/flash.lua
-- 作用：快速、可视化跳转（比 / 搜索 + n 快得多）
--
-- Flash 是“增强版跳转工具”：
-- - 用于在当前屏幕/窗口内快速定位字符、单词、行
-- - 不取代 / ?，而是用在「我眼睛已经看到目标，但不想慢慢挪光标」的场景
--
-- 使用原则：
-- - 少改默认逻辑（默认已经非常成熟）
-- - 只绑定常用、好记、不冲突的键位

return {
  "folke/flash.nvim",
  event = "VeryLazy", -- 懒加载，不影响启动速度
  opts = {
    ------------------------------------------------------------------
    -- 基础行为
    ------------------------------------------------------------------
    jump = {
      autojump = true, -- 唯一匹配时自动跳转（减少一次确认）
    },

    ------------------------------------------------------------------
    -- 标签显示（你现在字体 OK，Nerd Font 已配好）
    ------------------------------------------------------------------
    label = {
      uppercase = false,      -- 标签使用小写字母（更自然）
      rainbow = {
        enabled = true,       -- 多标签时用不同颜色，视觉区分更清晰
      },
    },

    ------------------------------------------------------------------
    -- 搜索行为
    ------------------------------------------------------------------
    search = {
      multi_window = false,   -- 默认只在当前窗口跳转（避免误跳）
      forward = true,
      wrap = false,
      mode = "exact",         -- 精确匹配（你想要的是“快准狠”，不是模糊）
    },

    ------------------------------------------------------------------
    -- 高亮
    ------------------------------------------------------------------
    highlight = {
      backdrop = true,        -- 背景暗化，突出可跳目标
    },

    ------------------------------------------------------------------
    -- Flash 各子功能模式
    ------------------------------------------------------------------
    modes = {
      -- 普通字符跳转（最常用）
      char = {
        enabled = true,
        keys = { "f", "F", "t", "T" }, -- 接管 f/F/t/T（行为增强，但不破坏直觉）
      },

      -- 单词/模式跳转（类似 leap / easymotion）
      search = {
        enabled = true,
      },

      -- 行跳转（大文件很好用）
      line = {
        enabled = true,
      },
    },
  },

  --------------------------------------------------------------------
  -- 快捷键绑定（统一写在插件内，避免 core/keymaps.lua require 顺序问题）
  --------------------------------------------------------------------
  keys = {
    ------------------------------------------------------------------
    -- 基础：搜索增强（替代 / 和 ?）
    ------------------------------------------------------------------
    {
      "s",
      function()
        require("flash").jump()
      end,
      mode = { "n", "x", "o" },
      desc = "Flash：快速字符/单词跳转",
    },

    ------------------------------------------------------------------
    -- 只在当前窗口搜索（更可控）
    ------------------------------------------------------------------
    {
      "S",
      function()
        require("flash").jump({ multi_window = false })
      end,
      mode = { "n", "x", "o" },
      desc = "Flash：当前窗口跳转",
    },

    ------------------------------------------------------------------
    -- Treesitter 模式（如果你以后重新启用 treesitter，会自动生效）
    -- 现在即使没启用 treesitter，也不会报错
    ------------------------------------------------------------------
    {
      "gs",
      function()
        require("flash").treesitter()
      end,
      mode = { "n", "x", "o" },
      desc = "Flash：基于语法结构跳转",
    },

    ------------------------------------------------------------------
    -- 远程操作（配合 operator-pending，很强，但不强迫你用）
    ------------------------------------------------------------------
    {
      "r",
      function()
        require("flash").remote()
      end,
      mode = "o",
      desc = "Flash：远程跳转（operator 模式）",
    },
  },
}

